				<b>Smog</b> – zjawisko atmosferyczne powstałe w wyniku wymieszania się mgły z dymem i spalinami. Etymologia pojęcia „smog” wskazuje na połączenie słów smoke (dym) i fog (mgła). Zanieczyszczenie powietrza, jakim jest smog, powstaje wskutek przedostawania się do atmosfery szkodliwych związków chemicznych, takich jak tlenki siarki i tlenek azotu oraz substancje stałe, czyli pyły zawieszone, a także kancerogenne wielopierścieniowe węglowodory aromatyczne.</br></br></br>

  <img src="img/4.jpg" alt="smog"> </br></br></br>
<h2>Szkodliwość</h2>
</br>
Wchodzące w skład smogu związki chemiczne, pyły i znaczna wilgotność stanowią zagrożenie dla zdrowia człowieka. Są czynnikami alergizującymi i mogą wywołać astmę oraz jej napady, a także powodować zaostrzenie przewlekłego zapalenia oskrzeli, niewydolność oddechową lub paraliż układu krwionośnego. Smog powoduje także zmniejszenie masy urodzeniowej noworodka, zwiększa zapadalność na nowotwory oraz przewlekłą obturacyjną chorobę płuc (POChP), jak i zwiększa prawdopodobieństwo wystąpienia astmy u dzieci.</br></br></br>

Według danych Światowej Organizacji Zdrowia (WHO) zanieczyszczenia powietrza zabijają co roku 6,5 mln ludzi na całym świecie. W Polsce jest to blisko 50 tys. osób rocznie – szacuje Europejska Agencja Środowiska. To, co trafia do naszych płuc, nie jest aż tak toksyczne jak dym papierosowy, ale nawet najbardziej uzależnieni od tytoniu nie palą bez przerwy. Natomiast zanieczyszczenia powietrza to coś, przed czym trudno jest nam się uchronić.</br></br></br>
  <img src="img/2.jpg" alt="smog"> </br></br></br>
<h2>Rodzaje smogu</h2></br>
Ze względu na miejsce i warunki powstawania oraz skład chemiczny wyróżnia się dwa rodzaje tego zjawiska: </br></br>
smog londyński w skład którego wchodzą: dwutlenek siarki, tlenki azotu, tlenki węgla, sadza oraz trudno opadające pyły. Występuje głównie w miesiącach od listopada do stycznia podczas inwersji temperatur w umiarkowanej strefie klimatycznej. </br></br>
smog typu Los Angeles (smog fotochemiczny, ozon troposferyczny) – powstaje przede wszystkim w miesiącach letnich, w strefach subtropikalnych. Składa się on z tlenków węgla, tlenków azotu i węglowodorów. Związki te ulegają później reakcjom fotochemicznym, w wyniku których powstają azotan nadtlenku acetylu, aldehydy oraz ozon. </br></br>
